  ! find the index for the MBWR-19 parameters
  function getMBWR19index(compid) result(idx)
    implicit none
    character(len=*), intent(in) :: compid
    integer :: idx
    logical :: found
    idx = 1
    found = .false.
    do while (idx <= nMbwr19Models .and. .not. found)
       if (trim(compid) /= trim(mbwr19Array(idx)%ComId) ) then
          idx = idx + 1
       else 
          found = .true.
       endif
    enddo
    if (.not. found) then
       idx = 0
    endif
    return
  end function getMBWR19index

