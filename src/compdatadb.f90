   !---------------------------------------------------------------------- >
   !> The function retun the index of the data record (in the module compdatadb)
   !! for the compoentent that is found
   !!
   !! \param compid Character string for a single component ID.
   !! \retval idx The array index of compdb.
   !!
   function getCompDBindex(compid) result(idx)
     use stringmod, only: str_eq
     implicit none

     character(len=*), intent(in) :: compid
     integer :: idx
     logical :: found
     idx = 1
     found = .false.
     do while (idx <= maxncdb .and. .not. found)
       if (str_eq(compid,compdb(idx)%ident)) then
           found = .true.
        else
         idx = idx + 1
       endif
     enddo
     if (.not. found) then
       idx = 0
     endif
     return
   end function getCompDBindex

  !---------------------------------------------------------------------- >
  !> The function copies the data from the compdb record to the acutil comp-record.
  !! \param dbindex The record index found from the getCompDBindex
  !!
  !! \retval component The component data - including the default choices where
  !! parameters for several methods are stord in the database.
  !!
  !!
  !! \author Geir S
  !!

  function copyFromDB (dbindex) result (component)
    use tpconst
    use compdata

    implicit none
    type (gendata) :: component
    integer, intent(in) :: dbindex
    integer :: iant, icp


    component%ident = compdb(dbindex)%ident
    component%formula = compdb(dbindex)%formula
    component%name = compdb(dbindex)%name

    component%mw = compdb(dbindex)%mw
    component%tc = compdb(dbindex)%tc
    component%pc = compdb(dbindex)%pc
    component%zc = compdb(dbindex)%zc
    component%acf = compdb(dbindex)%acf
    component%tb = compdb(dbindex)%tb

    component%psatcode = compdb(dbindex)%psatcode
    do iant = 1,3
       component%ant(iant) = compdb(dbindex)%ant(iant)
    enddo
    component%tantmin = compdb(dbindex)%tantmin
    component%tantmax = compdb(dbindex)%tantmax

    component%ncptype = compdb(dbindex)%ncptype !< Number of available cp-types
    component%cptype = compdb(dbindex)%cptype(1)

    do icp = 1,10
       component%cp(icp) = compdb(dbindex)%cp(1,icp)
    enddo
    component%tcpmin = compdb(dbindex)%tcpmin(1)
    component%tcpmax = compdb(dbindex)%tcpmax(1)

    component%zra = compdb(dbindex)%zra

    ! Deafult values, here a function must supply these
    ! href ans sref  must include the integration constants in ideal gas
    ! For enthalpy: -H(i,Tref) with href(i)=0, For Entropy: +log(pref)
    ! Should be included when enthalpy of formation is used.

    ! Init
    component%ci = 0.0
    component%ttr = 0.0
    component%ptr = 0.0
    component%href = 0.0
    component%sref = 0.0

  end function copyFromDB

